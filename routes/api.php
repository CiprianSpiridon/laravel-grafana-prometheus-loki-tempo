});

// Example API endpoints
Route::get('/health', function () {
    return response()->json([
        'status' => 'ok',
        'timestamp' => now()->toIso8601String(),
    ]);
});

Route::get('/metrics', function () {
    return response()->json([
        'cpu' => rand(1, 100),
        'memory' => rand(10, 80),
        'disk' => rand(20, 90),
    ]);
});
